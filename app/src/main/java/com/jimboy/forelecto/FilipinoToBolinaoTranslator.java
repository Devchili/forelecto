    public String translate(String word) {
        String cleanedWord = cleanWord(word);
        String closestMatch = findClosestMatch(cleanedWord);

        if (closestMatch != null) {
            return translationDict.get(closestMatch);
        }

        return "Translation not available";
    }

    private String cleanWord(String word) {
        // Remove special characters from the input word
        return word.replaceAll("[^a-zA-Z0-9 ]", "").toLowerCase();
    }

    private String findClosestMatch(String cleanedWord) {
        int minDistance = Integer.MAX_VALUE;
        String closestMatch = null;

        for (String key : translationDict.keySet()) {
            int distance = calculateLevenshteinDistance(cleanedWord, cleanWord(key));
            if (distance < minDistance) {
                minDistance = distance;
                closestMatch = key;
            }
        }

        // Adjust this threshold value according to your requirement
        if (minDistance <= 3) {
            return closestMatch;
        } else {
            return null; // No close match found
        }
    }


    private int calculateLevenshteinDistance(String word1, String word2) {
        int[][] dp = new int[word1.length() + 1][word2.length() + 1];

        for (int i = 0; i <= word1.length(); i++) {
            for (int j = 0; j <= word2.length(); j++) {
                if (i == 0) {
                    dp[i][j] = j;
                } else if (j == 0) {
                    dp[i][j] = i;
                } else {
                    dp[i][j] = min(
                            dp[i - 1][j - 1] + (word1.charAt(i - 1) == word2.charAt(j - 1) ? 0 : 1),
                            dp[i - 1][j] + 1,
                            dp[i][j - 1] + 1
                    );
                }
            }
        }

        return dp[word1.length()][word2.length()];
    }

    private int min(int x, int y, int z) {
        return Math.min(x, Math.min(y, z));
    }
